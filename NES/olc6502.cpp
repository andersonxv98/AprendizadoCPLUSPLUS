uint8_t olc6502::IND()
{   
    //prt -> ponteiro
    //prt_lo -> ponteiro_low
    //prt_hi -> ponteiro_high
	uint16_t ptr_lo = read(program_counter);
	program_counter++;
	uint16_t ptr_hi = read(program_counter);
	program_counter++;
    
	uint16_t ptr = (ptr_hi << 8) | ptr_lo;

	if (ptr_lo == 0x00FF) // Simulate page boundary hardware bug
	{
		adrr_absolut = (read(ptr & 0xFF00) << 8) | read(ptr + 0);
	}
	else // Behave normally
	{
		adrr_absolut = (read(ptr + 1) << 8) | read(ptr + 0);
	}
	
	return 0;
}


//acesso de endereço modo X offset indiretamente
uint8_t olc6502::IZX()
{
	uint16_t t = read(program_counter);
	program_counter++;

	uint16_t lo = read((uint16_t)(t + (uint16_t)x) & 0x00FF);
	uint16_t hi = read((uint16_t)(t + (uint16_t)x + 1) & 0x00FF);

	adrr_absolut = (hi << 8) | lo;
	
	return 0;
}

//acesso de endereço modo Y offset indiretamente
uint8_t olc6502::IZY()
{
	uint16_t t = read(program_counter);
	program_counter++;

	uint16_t lo = read(t & 0x00FF);
	uint16_t hi = read((t + 1) & 0x00FF);

	adrr_absolut = (hi << 8) | lo;
	adrr_absolut += y;
	
	if ((adrr_absolut & 0xFF00) != (hi << 8))
		return 1;
	else
		return 0;
}

uint8_t olc6502::fetch()
{
	if (!(lookup[opcode].addrmode == &olc6502::IMP))
		fetched = read(adrr_absolut);
	return fetched;
}

//Função de soma de bits
uint8_t olc6502::ADC()
{
	// pega o data que vai ser adicionado ao acumulador
	fetch();
	//a soma é feita no dominio de 16-bit  para emulação para capturar qualquer bit carregado

	temp = (uint16_t)a + (uint16_t)fetched + (uint16_t)GetFlag(C);
	
	// The carry flag out exists in the high byte bit 0
	SetFlag(C, temp > 255);
	
	// The Zero flag is set if the result is 0
	SetFlag(Z, (temp & 0x00FF) == 0);
	
	// The signed Overflow flag is set based on all that up there! :D
	SetFlag(V, (~((uint16_t)a ^ (uint16_t)fetched) & ((uint16_t)a ^ (uint16_t)temp)) & 0x0080);
	
	// The negative flag is set to the most significant bit of the result
	SetFlag(N, temp & 0x80);
	
	// Load the result into the accumulator (it's 8-bit dont forget!)
	a = temp & 0x00FF;
	
	// This instruction has the potential to require an additional clock cycle
	return 1;
}
//subtrráção de bytes
uint8_t olc6502::SBC()
{
	fetch();
	
	// Operating in 16-bit domain to capture carry out
	
	// We can invert the bottom 8 bits with bitwise xor
	uint16_t value = ((uint16_t)fetched) ^ 0x00FF;
	
	// Notice this is exactly the same as addition from here!
	temp = (uint16_t)a + value + (uint16_t)GetFlag(C);
	SetFlag(C, temp & 0xFF00);
	SetFlag(Z, ((temp & 0x00FF) == 0));
	SetFlag(V, (temp ^ (uint16_t)a) & (temp ^ value) & 0x0080);
	SetFlag(N, temp & 0x0080);
	a = temp & 0x00FF;
	return 1;
}

