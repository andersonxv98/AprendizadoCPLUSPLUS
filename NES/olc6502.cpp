//endereçamento RELATIVO: exclusivamente pra atender instruções, o endereço deve essistir entre -128 até 127 fora da breach
//impedindo de colocar diretamente fora do alcance de endereço  
uint8_t olc6502::REL()
{
	adrr_relative = read(program_counter);
	program_counter++;
	if (adrr_relative & 0x80)
		adrr_relative |= 0xFF00;
	return 0;
}

//endereco modo Absoluto: um endereço completo de 16-bit é carregado e usado.
uint8_t olc6502::ABS(){

    uint16_t lo = read(program_counter);
        program_counter++;
    uint16_t hi = read(program_counter);
        program_counter++;
    adrr_absolut  = (hi << 8) | lo;
    return 0;
}


//mesmo endereçamento absoluto, porem com valor do registro de x em offset
//verificando se o valor de endereço muda a pagina, caso seja verdade: adiciona um clock a mais;
